    private void crear_btnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_crear_btnActionPerformed
        Gui_Kun k = new Gui_Kun();
        //k.MatrizCajitas();
        //k.crearCajas(Integer.parseInt(this.n1Jtxt.getText()),Integer.parseInt(this.n2Jtxt.getText()));
        //Se obtiene la cantidad de filas a crear como una cadena de caracter
		String f = n1Jtxt.getText();

		//Se obtiene la cantidad de columnas a crear como una cadena de caracter
        String c = n2Jtxt.getText();
        
        filas = Integer.parseInt(f);
        columnas = Integer.parseInt(c);
		
        grafo_panel.removeAll();

        cajitas = new TextField[ filas ][ columnas ];
        
        for( int fila = 0 ; fila < filas; fila++ )
        {
            for( int columna = 0 ; columna < columnas; columna++ )
            {
                cajitas[fila][columna] = new TextField( 75 * columna, 30 * fila, 75, 30 );
                cajitas[fila][columna].setNombre(fila, columna);
                grafo_panel.add(cajitas[fila][columna] );
            }
        }
        
		grafo_panel.updateUI();
    }//GEN-LAST:event_crear_btnActionPerformed

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        //Matriz de pesos
        double [][] matriz_pesos = new double[filas][columnas];
        
        for( int i = 0 ; i < filas; i++ )
        {
            for( int j = 0 ; j < columnas; j++ )
            {
                matriz_pesos[i][j] = Double.parseDouble(cajitas[i][j].getText());
            }
        }    
        
        Grafo g = new Grafo(matriz_pesos, filas);
        g.solve();
       
    }//GEN-LAST:event_jButton1ActionPerformed

